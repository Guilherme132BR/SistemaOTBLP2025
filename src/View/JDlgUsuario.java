    public UsuarioOtb viewBean() {
        UsuarioOtb usuarioOtb = new UsuarioOtb();

        int id = Integer.valueOf(jFmtIdUsuario.getText());
        usuarioOtb.setIdUsuarioOtb(id);
        usuarioOtb.setNomeOtb(jTxtNome.getText());
        usuarioOtb.setApelidoOtb(jTxtApelido.getText());
        usuarioOtb.setCpfOtb(jFmtCPF.getText());
        SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy");
        try {
            usuarioOtb.setDataNascOtb(formato.parse(jFmtDataNasc.getText()));
        } catch (ParseException ex) {
            System.out.println("Erro data no ViewBean" + ex.getMessage());
        }

        usuarioOtb.setSenhaOtb(jPwfSenha.getText());
        usuarioOtb.setNivel(jCboNivel.getSelectedIndex());
        if (jChbAtivo.isSelected() == true) {
            usuarioOtb.setAtivo("S");
        } else {
            usuarioOtb.setAtivo("N");
        }
        return usuarioOtb;
    }

    public void beanView(UsuarioOtb usuarioOtb) {
        String id = String.valueOf(usuarioOtb.getIdUsuarioOtb());
        jFmtIdUsuario.setText(id);
        jTxtNome.setText(usuarioOtb.getNomeOtb());
        jTxtApelido.setText(usuarioOtb.getApelidoOtb());
        SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy");
        jFmtDataNasc.setText(formato.format(usuarioOtb.getDataNascOtb()));
        jPwfSenha.setText(usuarioOtb.getSenhaOtb());
        jCboNivel.setSelectedIndex(usuarioOtb.getNivel());
        jFmtCPF.setText(usuarioOtb.getCpfOtb());
        if (usuarioOtb.getAtivo().equals("S") == true) {
            jChbAtivo.setSelected(true);
        } else {
            jChbAtivo.setSelected(false);
        }
    }
