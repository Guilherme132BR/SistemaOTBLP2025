    private void jBtnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnExcluirActionPerformed
        // TODO add your handling code here:

        Util.habilitar(false, jFmtIdUsuario, jTxtNome, jTxtApelido, jFmtCPF, jFmtDataNasc, jPwfSenha, jChbAtivo, jCboNivel, jBtnCancelar, jBtnConfirmar, jBtnExcluir, jBtnAlterar);
        Util.habilitar(true, jBtnIncluir, jBtnPesquisar);

        if (Util.perguntar("deseja excluir o registro?") == true) {
            Util.habilitar(false, jFmtIdUsuario, jTxtNome, jTxtApelido, jFmtCPF, jFmtDataNasc, jPwfSenha, jChbAtivo, jCboNivel, jBtnCancelar, jBtnConfirmar);
            Util.habilitar(true, jBtnIncluir, jBtnAlterar, jBtnExcluir, jBtnPesquisar);
            UsuarioOtb usuarioOtb = viewBean();
            UsuarioOtb_DAO usuarios_DAO = new UsuarioOtb_DAO();
            usuarios_DAO.delete(usuarioOtb);
            Util.mensagem("Registro excluido");
        } else {
            Util.mensagem("Exclusão cancelada");
        }
        Util.limparCampos(jFmtIdUsuario, jTxtNome, jTxtApelido, jFmtCPF, jFmtDataNasc, jPwfSenha, jChbAtivo, jCboNivel);

    }//GEN-LAST:event_jBtnExcluirActionPerformed

    private void jBtnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnConfirmarActionPerformed
        // TODO add your handling code here:

        UsuarioOtb usuarioOtb = viewBean();
        UsuarioOtb_DAO usuarioOtb_DAO = new UsuarioOtb_DAO();

        if (incluindo == true) {
            usuarioOtb_DAO.insert(usuarioOtb);
        } else {
            usuarioOtb_DAO.update(usuarioOtb);
        }

        Util.habilitar(false, jFmtIdUsuario, jTxtNome, jTxtApelido, jFmtCPF, jFmtDataNasc, jPwfSenha, jChbAtivo, jCboNivel, jBtnCancelar, jBtnConfirmar, jBtnExcluir, jBtnAlterar);
        Util.habilitar(true, jBtnIncluir, jBtnPesquisar);
        Util.limparCampos(jFmtIdUsuario, jTxtNome, jTxtApelido, jFmtCPF, jFmtDataNasc, jPwfSenha, jChbAtivo, jCboNivel);

    }//GEN-LAST:event_jBtnConfirmarActionPerformed

